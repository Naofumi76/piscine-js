// More Or Less
console.log('---------- More or Less ----------')

function more(nb) {
    nb++
    return nb
}

function less(nb) {
    nb--
    return nb
}

function add(nb1, nb2) {
    return nb1 + nb2
}

function sub(nb1, nb2) {
    return nb1 - nb2
}

console.log(more(5))
console.log(less(5))
console.log(add(5,5))
console.log(sub(5,5))
